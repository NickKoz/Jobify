

export class Employee {
    
    name: string;
    surname: string;
    email: string;
    password: string;
    phone: string;
    photo: string;


    constructor(name: string, surname: string, email: string, password: string, phone: string, photo: string) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.password = password;
        this.phone = phone;
        this.photo = photo;
    }

}
